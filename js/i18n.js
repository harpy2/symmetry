/**
 * Translate a key. Returns the localized string or the key itself as fallback (Korean).
 * Supports simple {0}, {1} placeholder replacement.
 */
function t(key, ...args) {
  let str = key;
  if (LANG !== 'ko' && I18N[key] && I18N[key][LANG]) {
    str = I18N[key][LANG];
  }
  for (let i = 0; i < args.length; i++) {
    str = str.replace('{' + i + '}', args[i]);
  }
  return str;
}

function toggleLang() {
  LANG = LANG === 'ko' ? 'en' : 'ko';
  localStorage.setItem('sym_lang', LANG);
  location.reload();
}

function initLangToggle() {
  const label = document.getElementById('lang-label');
  if (label) label.textContent = LANG === 'ko' ? 'EN' : 'KO';
}

